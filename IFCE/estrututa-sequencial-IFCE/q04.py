base_maior = int(input('Informe o valor da base maior do trapézio: '))
base_menor = int(input('Informe o valor da base menor do trapézio: '))
altura = int(input('Informe o valor da altura do trapézio: '))
area = ((base_maior + base_menor) * altura) / 2

print(f'A area desse trapézi é igual a {area}cm²')
